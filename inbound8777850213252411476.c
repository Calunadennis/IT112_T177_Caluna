#include<stdio.h>
int main()
{
    int num1,num2,sum,dif,quo,pro;
    printf("Enter a number:");
    scanf("%d",&num2);

    sum=num1+num2;
    dif=num1-num2;
    quo=num1/num2;
    pro=num1*num2;

    printf("sum=%d\n",sum);
    printf("product=%d\n",pro);
    printf("difference=%d\n",dif);
    printf ("Quotient=%d\n",quo);
    return 0;
}